#include "libft.h"

int	ft_atoi(const char *s)
{
	int	x;
	int	y;

	x = 0;
	y = 1;
	while (*s == 32 || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-')
		y *= -1;
	if (*s == '-' || *s == '+')
		s++;
	while (*s >= '0' && *s <= '9')
	{
		x = x * 10 + *s - '0';
		s++;
	}
	return (x * y);
}
